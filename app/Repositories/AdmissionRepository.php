<?php

namespace App\Repositories;

use App\Models\Admission;
use App\Models\Student;
use App\Models\User;
use App\Models\Section;

use Illuminate\Support\Facades\Hash;

class AdmissionRepository
{
    /*
    |--------------------------------------------------------------------------
    | CREATE
    |--------------------------------------------------------------------------
    */

    public function create(array $data)
    {
        return Admission::create($data);
    }

    /*
    |--------------------------------------------------------------------------
    | GET ALL
    |--------------------------------------------------------------------------
    */

    public function getAll()
    {
        return Admission::with(
            'studentClass'
        )
        ->latest()
        ->get();
    }

    /*
    |--------------------------------------------------------------------------
    | FIND
    |--------------------------------------------------------------------------
    */

    public function find($id)
    {
        return Admission::with([

            'studentClass',

            'section',

            'state',

            'district',

        ])->findOrFail($id);
    }

    /*
    |--------------------------------------------------------------------------
    | UPDATE
    |--------------------------------------------------------------------------
    */

    public function update($id, $data)
    {
        $admission = $this->find($id);

        $filteredData = collect($data)->only([

            /*
            |--------------------------------------------------------------------------
            | STUDENT
            |--------------------------------------------------------------------------
            */

            'name',
            'dob',
            'class_id',
            'gender',
            'religion',
            'category',
            'state_id',
            'district_id',
            'aadhaar',

            /*
            |--------------------------------------------------------------------------
            | VERIFY
            |--------------------------------------------------------------------------
            */

            'section_id',

            /*
            |--------------------------------------------------------------------------
            | PARENTS
            |--------------------------------------------------------------------------
            */

            'father_name',
            'mother_name',

            'father_aadhaar',
            'mother_aadhaar',

            'father_education',
            'mother_education',

            'father_occupation',
            'mother_occupation',

            'father_mobile',
            'mother_mobile',

            'father_email',
            'mother_email',

            'emergency_contact',

            /*
            |--------------------------------------------------------------------------
            | ADDRESS
            |--------------------------------------------------------------------------
            */

            'pincode',
            'permanent_address',
            'current_address',

            /*
            |--------------------------------------------------------------------------
            | FILES
            |--------------------------------------------------------------------------
            */

            'photo',
            'birth_certificate',
            'aadhaar_doc',
            'family_photo',
            'father_aadhaar_doc',
            'mother_aadhaar_doc',

            /*
            |--------------------------------------------------------------------------
            | PAYMENT
            |--------------------------------------------------------------------------
            */

            'payment_id',
            'paid_at',

            /*
            |--------------------------------------------------------------------------
            | STATUS
            |--------------------------------------------------------------------------
            */

            'status',

        ])->toArray();

        $admission->update(
            $filteredData
        );

        return $admission;
    }

    /*
|--------------------------------------------------------------------------
| UPDATE STATUS
|--------------------------------------------------------------------------
*/

public function updateStatus(
    $id,
    $status,
    $extraData = []
)
{
    /*
    |--------------------------------------------------------------------------
    | FIND ADMISSION
    |--------------------------------------------------------------------------
    */

    $admission =
    $this->find($id);

    /*
    |--------------------------------------------------------------------------
    | DEFAULT DATA
    |--------------------------------------------------------------------------
    */

    $data = [

        'status' => $status

    ];

    /*
    |--------------------------------------------------------------------------
    | APPROVED
    |--------------------------------------------------------------------------
    */

    if($status === 'approved'){

        $data['approved_at'] =
        now();

        $data['approved_by'] =
        auth()->id();
    }

    /*
    |--------------------------------------------------------------------------
    | REJECTED
    |--------------------------------------------------------------------------
    */

    if($status === 'rejected'){

        $data['rejected_at'] =
        now();

        $data['rejected_by'] =
        auth()->id();
    }

    /*
    |--------------------------------------------------------------------------
    | MERGE EXTRA DATA
    |--------------------------------------------------------------------------
    */

    $data = array_merge(

        $data,

        $extraData
    );

    /*
    |--------------------------------------------------------------------------
    | UPDATE
    |--------------------------------------------------------------------------
    */

    $admission->update($data);

    /*
    |--------------------------------------------------------------------------
    | RETURN
    |--------------------------------------------------------------------------
    */

    return $admission->fresh();
}

    /*
    |--------------------------------------------------------------------------
    | CREATE STUDENT
    |--------------------------------------------------------------------------
    */

    public function createStudentIfNotExists($admission)
    {
        $exists = Student::where(

            'admission_id',

            $admission->id

        )->exists();

        if ($exists) {

            return;
        }

        /*
        |--------------------------------------------------------------------------
        | SECTION
        |--------------------------------------------------------------------------
        */

        $section = $admission->section_id

            ? Section::find(
                $admission->section_id
            )

            : Section::where(

                'class_id',

                $admission->class_id

            )
            ->where('status', 1)
            ->withCount('students')
            ->orderBy('students_count')
            ->first();

        /*
        |--------------------------------------------------------------------------
        | SECTION VALIDATION
        |--------------------------------------------------------------------------
        */

        if (!$section) {

            throw new \Exception(

                "No section found for class ID: "

                . $admission->class_id
            );
        }

        /*
        |--------------------------------------------------------------------------
        | PHONE
        |--------------------------------------------------------------------------
        */

        $phone = $admission->father_mobile

            ?: (

                $admission->mother_mobile

                ?: '0000000000'
            );

        /*
        |--------------------------------------------------------------------------
        | CREATE STUDENT
        |--------------------------------------------------------------------------
        */

        Student::create([

            'name' => $admission->name,

            'email' =>

                $admission->father_email

                ?? $admission->mother_email,

            'phone' => $phone,

            'class_id' => $admission->class_id,

            'section_id' => $section->id,

            'admission_id' => $admission->id,

            'status' => 1,

        ]);
    }

    /*
    |--------------------------------------------------------------------------
    | CREATE STUDENT USER
    |--------------------------------------------------------------------------
    */

    public function createStudentUser(
        $admission,
        $regNo,
        $password
    ) {

        /*
        |--------------------------------------------------------------------------
        | CHECK EXISTING STUDENT USER
        |--------------------------------------------------------------------------
        */

        $exists = User::where(

            'registration_no',

            $regNo

        )->first();

        /*
        |--------------------------------------------------------------------------
        | RETURN EXISTING USER
        |--------------------------------------------------------------------------
        */

        if ($exists) {

            $admission->update([

                'student_user_id' => $exists->id,

                'registration_no' => $regNo,

            ]);

            return $exists;
        }

        /*
        |--------------------------------------------------------------------------
        | EMAIL
        |--------------------------------------------------------------------------
        */

        $email = strtolower(

            $regNo

        ) . '@school.com';

        /*
        |--------------------------------------------------------------------------
        | CREATE USER
        |--------------------------------------------------------------------------
        */

        $user = User::create([

            'name' => $admission->name,

            'email' => $email,

            'password' => Hash::make($password),

            'registration_no' => $regNo,

            'role' => 'student',

            'status' => 1,

            'phone' => $admission->father_mobile,

            'address' => $admission->current_address,

        ]);

        /*
        |--------------------------------------------------------------------------
        | ASSIGN ROLE
        |--------------------------------------------------------------------------
        */

        $user->assignRole(
            'student'
        );

        /*
        |--------------------------------------------------------------------------
        | UPDATE ADMISSION
        |--------------------------------------------------------------------------
        */

        $admission->update([

            'student_user_id' => $user->id,

            'registration_no' => $regNo,

            'verification_status' => 'approved',

            'status' => Admission::STATUS_APPROVED,

        ]);

        return $user;
    }

    /*
    |--------------------------------------------------------------------------
    | CREATE PARENT USER
    |--------------------------------------------------------------------------
    */

    public function createParentUser(
        $admission,
        $password
    ) {

        /*
        |--------------------------------------------------------------------------
        | EMAIL
        |--------------------------------------------------------------------------
        */

        $email = $admission->father_email
            ?: $admission->mother_email;

        /*
        |--------------------------------------------------------------------------
        | VALIDATE EMAIL
        |--------------------------------------------------------------------------
        */

        if (!$email) {

            return null;
        }

        /*
        |--------------------------------------------------------------------------
        | FIND EXISTING USER
        |--------------------------------------------------------------------------
        */

        $user = User::where(
            'email',
            $email
        )->first();

        /*
        |--------------------------------------------------------------------------
        | CREATE USER IF NOT EXISTS
        |--------------------------------------------------------------------------
        */

        if (!$user) {

            $user = User::create([

                'name' =>

                    $admission->father_name
                    ?: $admission->mother_name,

                'email' => $email,

                'password' => Hash::make($password),

                'status' => 1,

            ]);

            /*
            |--------------------------------------------------------------------------
            | ASSIGN ROLE
            |--------------------------------------------------------------------------
            */

            $user->assignRole(
                'parent'
            );
        }

        /*
        |--------------------------------------------------------------------------
        | UPDATE ADMISSION
        |--------------------------------------------------------------------------
        */

        $admission->update([

            'parent_user_id' => $user->id,

        ]);

        return $user;
    }

  /*
|--------------------------------------------------------------------------
| APPROVED / FEE ELIGIBLE ADMISSIONS
|--------------------------------------------------------------------------
*/

public function getApprovedAdmissions()
{
    return Admission::with([

            'studentClass:id,name'

        ])

        /*
        |--------------------------------------------------------------------------
        | VERIFIED STUDENTS ONLY
        |--------------------------------------------------------------------------
        */

        ->where(
            'verification_status',
            'approved'
        )

        /*
        |--------------------------------------------------------------------------
        | LATEST FIRST
        |--------------------------------------------------------------------------
        */

        ->latest()

        /*
        |--------------------------------------------------------------------------
        | REQUIRED FIELDS
        |--------------------------------------------------------------------------
        */

        ->get([

            'id',

            'name',

            'registration_no',

            'class_id',

            'total_fee',

            'paid_amount',

            'due_amount',

            'fee_due_date',

            'verification_status',

            'payment_type',

            'status'

        ]);
}
}