<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Teacher extends Model
{
    /*
    |--------------------------------------------------------------------------
    | TABLE
    |--------------------------------------------------------------------------
    */

    protected $table = 'teachers';

    /*
    |--------------------------------------------------------------------------
    | FILLABLE
    |--------------------------------------------------------------------------
    */

    protected $fillable = [

        /*
        |--------------------------------------------------------------------------
        | BASIC INFO
        |--------------------------------------------------------------------------
        */

        'name',

        'email',

        'phone',

        'photo',

        /*
        |--------------------------------------------------------------------------
        | ACADEMIC
        |--------------------------------------------------------------------------
        */

        'employee_id',

        'qualification',

        'experience',

        'specialization',

        /*
        |--------------------------------------------------------------------------
        | ADDRESS
        |--------------------------------------------------------------------------
        */

        'address',

        /*
        |--------------------------------------------------------------------------
        | STATUS
        |--------------------------------------------------------------------------
        */

        'status',

    ];

    /*
    |--------------------------------------------------------------------------
    | APPENDS
    |--------------------------------------------------------------------------
    */

    protected $appends = [

        'photo_url',

        'status_badge'

    ];

    /*
    |--------------------------------------------------------------------------
    | ONLINE CLASSES
    |--------------------------------------------------------------------------
    */

    public function onlineClasses()
    {
        return $this->hasMany(

            OnlineClass::class,

            'teacher_id'

        );
    }

    /*
    |--------------------------------------------------------------------------
    | SUBJECTS
    |--------------------------------------------------------------------------
    */

    public function subjects()
    {
        return $this->belongsToMany(

            Subject::class,

            'teacher_subjects',

            'teacher_id',

            'subject_id'

        );
    }

    /*
    |--------------------------------------------------------------------------
    | CLASSES
    |--------------------------------------------------------------------------
    */

    public function classes()
    {
        return $this->belongsToMany(

            SchoolClass::class,

            'teacher_classes',

            'teacher_id',

            'class_id'

        );
    }

    /*
    |--------------------------------------------------------------------------
    | PHOTO URL
    |--------------------------------------------------------------------------
    */

    public function getPhotoUrlAttribute()
    {
        if($this->photo){

            return asset(

                'uploads/teachers/' . $this->photo

            );
        }

        return asset(

            'assets/images/default-user.png'

        );
    }

    /*
    |--------------------------------------------------------------------------
    | STATUS BADGE
    |--------------------------------------------------------------------------
    */

    public function getStatusBadgeAttribute()
    {
        if($this->status == 'active'){

            return '

                <span class="badge bg-success">

                    Active

                </span>

            ';
        }

        return '

            <span class="badge bg-danger">

                Inactive

            </span>

        ';
    }

    /*
    |--------------------------------------------------------------------------
    | HELPERS
    |--------------------------------------------------------------------------
    */

    public function isActive()
    {
        return $this->status === 'active';
    }
}