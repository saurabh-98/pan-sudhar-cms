<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Student extends Model
{
    protected $fillable = [

        /*
        |--------------------------------------------------------------------------
        | BASIC INFO
        |--------------------------------------------------------------------------
        */

        'name',

        'email',

        'phone',

        'photo',

        /*
        |--------------------------------------------------------------------------
        | ACADEMIC
        |--------------------------------------------------------------------------
        */

        'class_id',

        'section_id',

        'roll_number',

        /*
        |--------------------------------------------------------------------------
        | STATUS
        |--------------------------------------------------------------------------
        */

        'status',

        /*
        |--------------------------------------------------------------------------
        | ADMISSION LINK
        |--------------------------------------------------------------------------
        */

        'admission_id'
    ];

    /*
    |--------------------------------------------------------------------------
    | ADMISSION RELATION
    |--------------------------------------------------------------------------
    */

    public function admission()
    {
        return $this->belongsTo(
            Admission::class
        );
    }

    /*
    |--------------------------------------------------------------------------
    | CLASS RELATION
    |--------------------------------------------------------------------------
    */

    public function class()
    {
        return $this->belongsTo(
            SchoolClass::class
        );
    }

    /*
    |--------------------------------------------------------------------------
    | SECTION RELATION
    |--------------------------------------------------------------------------
    */

    public function section()
    {
        return $this->belongsTo(
            Section::class
        );
    }
}