<?php

use Illuminate\Support\Facades\File;

if (!function_exists('is_vercel')) {

    /*
    |--------------------------------------------------------------------------
    | CHECK VERCEL
    |--------------------------------------------------------------------------
    */

    function is_vercel(): bool
    {
        return (bool) env('VERCEL');
    }
}

if (!function_exists('upload_base_path')) {

    /*
    |--------------------------------------------------------------------------
    | BASE UPLOAD PATH
    |--------------------------------------------------------------------------
    */

    function upload_base_path(): string
    {
        /*
        |--------------------------------------------------------------------------
        | VERCEL
        |--------------------------------------------------------------------------
        */

        if (is_vercel()) {

            return sys_get_temp_dir();
        }

        /*
        |--------------------------------------------------------------------------
        | LOCALHOST / CPANEL / VPS
        |--------------------------------------------------------------------------
        */

        return storage_path('app/public');
    }
}

if (!function_exists('upload_path')) {

    /*
    |--------------------------------------------------------------------------
    | FULL UPLOAD PATH
    |--------------------------------------------------------------------------
    */

    function upload_path(
        string $folder = ''
    ): string {

        $basePath = upload_base_path();

        $fullPath =

            rtrim(
                $basePath,
                DIRECTORY_SEPARATOR
            )

            . DIRECTORY_SEPARATOR .

            trim(
                $folder,
                DIRECTORY_SEPARATOR
            );

        /*
        |--------------------------------------------------------------------------
        | CREATE DIRECTORY
        |--------------------------------------------------------------------------
        */

        if (!File::exists($fullPath)) {

            File::makeDirectory(

                $fullPath,

                0777,

                true,

                true
            );
        }

        return $fullPath;
    }
}

if (!function_exists('store_uploaded_file')) {

    /*
    |--------------------------------------------------------------------------
    | STORE FILE
    |--------------------------------------------------------------------------
    */

    function store_uploaded_file(
        $file,
        string $folder
    ): ?string {

        if (!$file) {

            return null;
        }

        /*
        |--------------------------------------------------------------------------
        | DESTINATION
        |--------------------------------------------------------------------------
        */

        $destination =
            upload_path($folder);

        /*
        |--------------------------------------------------------------------------
        | FILE NAME
        |--------------------------------------------------------------------------
        */

        $filename =

            time()

            . '_'

            . uniqid()

            . '.'

            . $file->getClientOriginalExtension();

        /*
        |--------------------------------------------------------------------------
        | MOVE FILE
        |--------------------------------------------------------------------------
        */

        $file->move(

            $destination,

            $filename

        );

        /*
        |--------------------------------------------------------------------------
        | RETURN CLEAN RELATIVE PATH
        |--------------------------------------------------------------------------
        */

        return ltrim(

            trim($folder, '/')

            . '/'

            . $filename,

            '/'

        );
    }
}
if (!function_exists('file_url')) {

    /*
    |--------------------------------------------------------------------------
    | FILE URL
    |--------------------------------------------------------------------------
    */

    function file_url(
        ?string $path
    ): ?string {

        if (!$path) {

            return null;
        }

        /*
        |--------------------------------------------------------------------------
        | VERCEL
        |--------------------------------------------------------------------------
        */

        if (is_vercel()) {

            return url(

                '/temp-file/' .

                ltrim($path, '/')

            );
        }

        /*
        |--------------------------------------------------------------------------
        | LOCALHOST / CPANEL / VPS
        |--------------------------------------------------------------------------
        */

        return asset(
            'storage/' . ltrim($path, '/')
        );
    }
}

if (!function_exists('file_exists_custom')) {

    /*
    |--------------------------------------------------------------------------
    | FILE EXISTS
    |--------------------------------------------------------------------------
    */

    function file_exists_custom(
        ?string $path
    ): bool {

        if (!$path) {

            return false;
        }

        /*
        |--------------------------------------------------------------------------
        | VERCEL
        |--------------------------------------------------------------------------
        */

        if (is_vercel()) {

            return File::exists(

                sys_get_temp_dir()

                . '/'

                . ltrim($path, '/')

            );
        }

        /*
        |--------------------------------------------------------------------------
        | LOCALHOST
        |--------------------------------------------------------------------------
        */

        return File::exists(

            storage_path(

                'app/public/' .

                ltrim($path, '/')

            )

        );
    }
}