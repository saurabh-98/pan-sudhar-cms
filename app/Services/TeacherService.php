<?php

namespace App\Services;

use App\Repositories\TeacherRepository;

use App\DTO\TeacherDTO;

use Illuminate\Support\Facades\DB;

use Illuminate\Support\Facades\Storage;

class TeacherService
{
    /*
    |--------------------------------------------------------------------------
    | CONSTRUCTOR
    |--------------------------------------------------------------------------
    */

    public function __construct(

        protected TeacherRepository $repo

    ) {}

    /*
    |--------------------------------------------------------------------------
    | GET ALL
    |--------------------------------------------------------------------------
    */

    public function getAll()
    {
        return $this->repo->all();
    }

    /*
    |--------------------------------------------------------------------------
    | CREATE
    |--------------------------------------------------------------------------
    */

    public function create(TeacherDTO $dto)
    {
        return DB::transaction(function () use ($dto) {

            /*
            |--------------------------------------------------------------------------
            | PHOTO
            |--------------------------------------------------------------------------
            */

            $photoPath = null;

            if ($dto->photo) {

                $photoPath = $dto->photo->store(

                    'teachers',

                    'public'

                );
            }

            /*
            |--------------------------------------------------------------------------
            | CREATE TEACHER
            |--------------------------------------------------------------------------
            */

            $teacher = $this->repo->store(

                $dto->toArray($photoPath)

            );

            /*
            |--------------------------------------------------------------------------
            | SUBJECTS
            |--------------------------------------------------------------------------
            */

            if ($dto->subjects) {

                $teacher->subjects()->sync(

                    $dto->subjects

                );
            }

            /*
            |--------------------------------------------------------------------------
            | CLASSES
            |--------------------------------------------------------------------------
            */

            if ($dto->classes) {

                $teacher->classes()->sync(

                    $dto->classes

                );
            }

            return $teacher;
        });
    }

    /*
    |--------------------------------------------------------------------------
    | UPDATE
    |--------------------------------------------------------------------------
    */

    public function update($id, TeacherDTO $dto)
    {
        return DB::transaction(function () use ($id, $dto) {

            /*
            |--------------------------------------------------------------------------
            | GET TEACHER
            |--------------------------------------------------------------------------
            */

            $teacher = $this->repo->find($id);

            /*
            |--------------------------------------------------------------------------
            | PHOTO
            |--------------------------------------------------------------------------
            */

            $photoPath = $teacher->photo;

            if ($dto->photo) {

                /*
                |--------------------------------------------------------------------------
                | DELETE OLD PHOTO
                |--------------------------------------------------------------------------
                */

                if (

                    $teacher->photo

                    &&

                    Storage::disk('public')->exists(

                        $teacher->photo

                    )

                ) {

                    Storage::disk('public')->delete(

                        $teacher->photo

                    );
                }

                /*
                |--------------------------------------------------------------------------
                | STORE NEW PHOTO
                |--------------------------------------------------------------------------
                */

                $photoPath = $dto->photo->store(

                    'teachers',

                    'public'

                );
            }

            /*
            |--------------------------------------------------------------------------
            | UPDATE TEACHER
            |--------------------------------------------------------------------------
            */

            $teacher = $this->repo->update(

                $id,

                $dto->toArray($photoPath)

            );

            /*
            |--------------------------------------------------------------------------
            | SUBJECTS
            |--------------------------------------------------------------------------
            */

            $teacher->subjects()->sync(

                $dto->subjects ?? []

            );

            /*
            |--------------------------------------------------------------------------
            | CLASSES
            |--------------------------------------------------------------------------
            */

            $teacher->classes()->sync(

                $dto->classes ?? []

            );

            return $teacher;
        });
    }

    /*
    |--------------------------------------------------------------------------
    | DELETE
    |--------------------------------------------------------------------------
    */

    public function delete($id)
    {
        return DB::transaction(function () use ($id) {

            /*
            |--------------------------------------------------------------------------
            | GET TEACHER
            |--------------------------------------------------------------------------
            */

            $teacher = $this->repo->find($id);

            /*
            |--------------------------------------------------------------------------
            | DELETE PHOTO
            |--------------------------------------------------------------------------
            */

            if (

                $teacher->photo

                &&

                Storage::disk('public')->exists(

                    $teacher->photo

                )

            ) {

                Storage::disk('public')->delete(

                    $teacher->photo

                );
            }

            /*
            |--------------------------------------------------------------------------
            | DETACH RELATIONS
            |--------------------------------------------------------------------------
            */

            $teacher->subjects()->detach();

            $teacher->classes()->detach();

            /*
            |--------------------------------------------------------------------------
            | DELETE
            |--------------------------------------------------------------------------
            */

            return $this->repo->delete($id);
        });
    }
}