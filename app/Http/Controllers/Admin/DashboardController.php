<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;

use App\Services\DashboardService;

use Illuminate\View\View;

class DashboardController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | SERVICE
    |--------------------------------------------------------------------------
    */

    protected DashboardService $service;

    /*
    |--------------------------------------------------------------------------
    | CONSTRUCTOR
    |--------------------------------------------------------------------------
    */

    public function __construct(
        DashboardService $service
    )
    {
        $this->service = $service;
    }

    /*
    |--------------------------------------------------------------------------
    | DASHBOARD
    |--------------------------------------------------------------------------
    */

    public function index(): View
    {

        /*
        |--------------------------------------------------------------------------
        | GET DASHBOARD DATA
        |--------------------------------------------------------------------------
        */

        $data = $this->service->getDashboardData();

        /*
        |--------------------------------------------------------------------------
        | RETURN VIEW
        |--------------------------------------------------------------------------
        */

        return view(

            'admin.dashboard',

            [

                /*
                |--------------------------------------------------------------------------
                | PAN
                |--------------------------------------------------------------------------
                */

                'totalPanApplications' =>

                    $data['totalPanApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | ITR
                |--------------------------------------------------------------------------
                */

                'totalItrApplications' =>

                    $data['totalItrApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | ASSIGNED
                |--------------------------------------------------------------------------
                */

                'assignedApplications' =>

                    $data['assignedApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | COMPLETED
                |--------------------------------------------------------------------------
                */

                'completedApplications' =>

                    $data['completedApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | FRESH
                |--------------------------------------------------------------------------
                */

                'freshApplications' =>

                    $data['freshApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | PROCESSING
                |--------------------------------------------------------------------------
                */

                'processingApplications' =>

                    $data['processingApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | PENDING
                |--------------------------------------------------------------------------
                */

                'pendingApplications' =>

                    $data['pendingApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | APPROVED
                |--------------------------------------------------------------------------
                */

                'approvedApplications' =>

                    $data['approvedApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | REJECTED
                |--------------------------------------------------------------------------
                */

                'rejectedApplications' =>

                    $data['rejectedApplications'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | TODAY UPLOADS
                |--------------------------------------------------------------------------
                */

                'todayUploads' =>

                    $data['todayUploads'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | REVENUE
                |--------------------------------------------------------------------------
                */

                'totalRevenue' =>

                    $data['totalRevenue'] ?? 0,

                /*
                |--------------------------------------------------------------------------
                | CHART
                |--------------------------------------------------------------------------
                */

                'months' =>

                    $data['months'] ?? [],

                'chartData' =>

                    $data['chartData'] ?? [],

            ]

        );

    }
}